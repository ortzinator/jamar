    {
        $policy = Policy::findOrFail($request['policy_id']);

        $policy
            ->history()
            ->create(
                $request->only('message', 'value', 'user_id', 'event_type')
            );

        session()->flash('message', 'History updated');
        return Redirect::back();
    }

